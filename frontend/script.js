function showMemeDetail(id) {
  const meme = memesData.find((m) => m.id === id);
  if (!meme) {
    console.error("Мем не найден!");
    return;
  }

  document.getElementById("modalTitle").innerText = meme.name;
  document.getElementById("modalDesc").innerHTML =
    `<strong>📜 История мема:</strong><br><br>${meme.desc}<br><br><strong>🏷 Теги:</strong> ${meme.tags.join(", ")}`;

  const modalImg = document.getElementById("modalImg");
  if (meme.image) {
    modalImg.innerHTML = `<img src="${meme.image}" alt="${meme.alt || meme.name}" style="width:100%; height:auto; border-radius:16px;" onerror="this.parentElement.innerHTML='<div style=\'padding:40px; text-align:center;\'>🐱</div>'">`;
  } else {
    modalImg.innerHTML = `<div style="background:linear-gradient(135deg,#e9e1d8,#d4c9be); padding:40px; text-align:center; border-radius:16px;"><div style="font-size:4rem;">🐱</div><div>${meme.name}</div></div>`;
  }

  const similarIds = meme.similarIds || [];
  const similarMemes = similarIds
    .map((sid) => memesData.find((m) => m.id === sid))
    .filter(Boolean);
  const relatedList = document.getElementById("relatedMemesList");
  if (relatedList) {
    relatedList.innerHTML = similarMemes.length
      ? similarMemes
          .map(
            (m) =>
              `<span style="background:#f0e9e2; padding:8px 16px; border-radius:30px; cursor:pointer; display:inline-block; margin:5px;" onclick="showMemeDetail(${m.id})">🐱 ${m.name}</span>`,
          )
          .join("")
      : '<span style="color:#999;">Нет похожих мемов</span>';
  }

  const modal = document.getElementById("memeModal");
  if (modal) modal.style.display = "flex";
}

function closeModal() {
  const modal = document.getElementById("memeModal");
  if (modal) modal.style.display = "none";
}

function attachCardEvents() {
  document.querySelectorAll(".meme-card").forEach((card) => {
    card.addEventListener("click", () => {
      const id = parseInt(card.getAttribute("data-id"));
      if (!isNaN(id)) showMemeDetail(id);
    });
  });
}

function renderMemeCard(m, descLen = 80) {
  return `
        <div class="meme-card" data-id="${m.id}">
            <div class="card-img">
                ${m.image ? `<img src="${m.image}" alt="${m.name}" style="width:100%;height:100%;object-fit:cover;" onerror="this.style.display='none';this.parentElement.innerHTML='🐱'">` : '<div style="font-size:3rem;">🐱</div>'}
            </div>
            <div class="card-content">
                <h3>${m.name}</h3>
                <div class="epoch-badge">${m.epoch === "2000s" ? "2000-е" : m.epoch === "2010s" ? "2010-е" : "2020-е"}</div>
                <p>${m.desc.substring(0, descLen)}...</p>
            </div>
        </div>`;
}

function renderGallery(filterEpoch = "all") {
  let filtered =
    filterEpoch === "all"
      ? memesData
      : memesData.filter((m) => m.epoch === filterEpoch);
  const galleryGrid = document.getElementById("galleryGrid");
  if (galleryGrid) {
    galleryGrid.innerHTML = filtered.map((m) => renderMemeCard(m)).join("");
    attachCardEvents();
  }
}

