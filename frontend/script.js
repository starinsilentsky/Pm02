const ALL_SECTIONS = [
  "homeSection",
  "gallerySection",
  "timelineSection",
  "archiveSection",
  "interactiveSection",
  "blogSection",
];

function showSection(section) {
  ALL_SECTIONS.forEach((s) => {
    const el = document.getElementById(s);
    if (el) el.style.display = "none";
  });

  if (section === "home")
    document.getElementById("homeSection").style.display = "block";
  if (section === "gallery") {
    document.getElementById("gallerySection").style.display = "block";
    renderGallery();
  }
  if (section === "timeline") {
    document.getElementById("timelineSection").style.display = "block";
    renderTimeline();
  }
  if (section === "archive") {
    document.getElementById("archiveSection").style.display = "block";
    renderTags();
  }
  if (section === "interactive") {
    document.getElementById("interactiveSection").style.display = "block";
    loadQuiz();
    renderVote();
  }
  if (section === "blog") {
    document.getElementById("blogSection").style.display = "block";
    renderGlossary();
  }

  document.querySelectorAll(".nav-tabs a").forEach((a) => {
    a.classList.toggle("active", a.getAttribute("data-section") === section);
  });
}

function initNavigation() {
  document.querySelectorAll(".nav-tabs a").forEach((link) => {
    link.addEventListener("click", (e) => {
      e.preventDefault();
      showSection(link.getAttribute("data-section"));
    });
  });

  const logo = document.querySelector(".logo a");
  if (logo) {
    logo.addEventListener("click", (e) => {
      e.preventDefault();
      showSection("home");
    });
  }
}

document.addEventListener("DOMContentLoaded", function () {
  console.log("Сайт загружен, инициализация...");

  const closeBtn = document.querySelector(".close-modal");
  if (closeBtn) closeBtn.onclick = closeModal;

  const modal = document.getElementById("memeModal");
  if (modal) {
    modal.addEventListener("click", (e) => {
      if (e.target === modal) closeModal();
    });
  }

  initNavigation();
  renderTimeline();
  renderGlossary();
  loadQuiz();
  renderVote();

  const featured = memesData.slice(0, 4);
  const featuredGrid = document.getElementById("featuredMemesGrid");
  if (featuredGrid) {
    featuredGrid.innerHTML = featured
      .map((m) => renderMemeCard(m, 60))
      .join("");
    attachCardEvents();
  }

  const exploreBtn = document.getElementById("exploreGalleryBtn");
  if (exploreBtn) exploreBtn.onclick = () => showSection("gallery");

  const randomBtn = document.getElementById("randomMemeBtn");
  if (randomBtn)
    randomBtn.onclick = () => {
      const randomId = Math.floor(Math.random() * memesData.length) + 1;
      showMemeDetail(randomId);
    };

  const nextQuizBtn = document.getElementById("nextQuizBtn");
  if (nextQuizBtn) nextQuizBtn.onclick = nextQuiz;

  const proposeBtn = document.getElementById("proposeMemeBtn");
  if (proposeBtn)
    proposeBtn.onclick = () =>
      alert("📝 Заявка принята! Напишите нам: admin@memorium.ru");

  const authBtn = document.getElementById("authBtn");
  if (authBtn) authBtn.onclick = () => alert("🔐 Авторизация (демо-режим)");

  const searchBtn = document.getElementById("searchBtn");
  const searchInput = document.getElementById("globalSearch");
  if (searchBtn && searchInput) {
    searchBtn.onclick = () => {
      const term = searchInput.value.toLowerCase();
      const found = memesData.filter((m) =>
        m.name.toLowerCase().includes(term),
      );
      if (found.length) showMemeDetail(found[0].id);
      else alert('Мем не найден. Попробуйте: "кот", "Бу", "Степан"');
    };
    searchInput.addEventListener("keypress", (e) => {
      if (e.key === "Enter") searchBtn.click();
    });
  }

  document.querySelectorAll(".filter-btn[data-epoch]").forEach((btn) => {
    btn.addEventListener("click", function () {
      document
        .querySelectorAll(".filter-btn")
        .forEach((b) => b.classList.remove("active"));
      this.classList.add("active");
      renderGallery(this.getAttribute("data-epoch"));
    });
  });

  const generateBtn = document.getElementById("generateMemeBtn");
  if (generateBtn) {
    generateBtn.onclick = () => {
      const top = document.getElementById("memeTopText")?.value || "";
      const bottom = document.getElementById("memeBottomText")?.value || "";
      const preview = document.getElementById("memePreview");
      if (preview) {
        preview.innerHTML = `<div style="background:#fff; border-radius:24px; padding:20px; text-align:center; border:2px dashed #cf6f2e;">
                    <strong style="font-size:1.2rem;">${top || "ВЕРХНИЙ ТЕКСТ"}</strong><br>
                    <div style="font-size:4rem; margin:15px 0;">🐱 🍌</div>
                    <em style="color:#666;">${bottom || "НИЖНИЙ ТЕКСТ"}</em>
                </div>`;
      }
    };
  }

  showSection("home");
  console.log("Инициализация завершена!");
});
