let quizIdx = 0;

function loadQuiz() {
  const q = quizBank[quizIdx];
  const quizQuestion = document.getElementById("quizQuestion");
  const quizOptions = document.getElementById("quizOptions");
  const quizFeedback = document.getElementById("quizFeedback");

  if (quizQuestion) quizQuestion.innerText = q.q;
  if (quizFeedback) quizFeedback.innerText = "";

  if (quizOptions) {
    const shuffledOptions = [...q.options].sort(() => Math.random() - 0.5);
    quizOptions.innerHTML = shuffledOptions
      .map(
        (opt) =>
          `<button class="filter-btn quiz-opt" data-opt="${opt}">${opt}</button>`,
      )
      .join("");

    document.querySelectorAll(".quiz-opt").forEach((btn) => {
      btn.addEventListener("click", () => {
        const selected = btn.getAttribute("data-opt");
        if (selected === q.a) {
          if (quizFeedback)
            quizFeedback.innerText = "✅ Верно! Отлично, вы знаток мемов!";
          btn.style.background = "#90EE90";
        } else {
          if (quizFeedback)
            quizFeedback.innerText = `❌ Неверно! Правильный ответ: ${q.a}`;
          btn.style.background = "#FFB6C1";
          document.querySelectorAll(".quiz-opt").forEach((b) => {
            if (b.getAttribute("data-opt") === q.a)
              b.style.background = "#90EE90";
          });
        }
      });
    });
  }
}

function nextQuiz() {
  quizIdx = (quizIdx + 1) % quizBank.length;
  loadQuiz();
}

