function renderTimeline() {
  const eraMap = document.getElementById("eraMap");
  const eraContext = document.getElementById("eraContext");
  if (!eraMap) return;

  eraMap.innerHTML = ERAS.map((era) => {
    const eraMemes = memesData.filter((m) => m.epoch === era.key);
    return `
            <div class="timeline-era-column" style="flex:1; min-width:200px; margin:0 8px;">
                <div style="background:${era.color}; padding:16px; border-radius:16px 16px 0 0; text-align:center;">
                    <span style="font-size:2rem;">${era.icon}</span>
                    <h3 style="margin:8px 0 4px;">${era.name}</h3>
                    <p style="margin:0; font-size:0.85rem;">${era.years}</p>
                    <span style="font-size:0.75rem;">${eraMemes.length} мемов</span>
                </div>
                <div style="background:#f8f4ef; padding:12px; border-radius:0 0 16px 16px; max-height:400px; overflow-y:auto;">
                    ${eraMemes
                      .map(
                        (m) => `
                        <div class="tl-meme-card" data-id="${m.id}" style="background:white; margin-bottom:12px; padding:10px; border-radius:12px; cursor:pointer; display:flex; gap:12px; align-items:center;">
                            <div style="width:50px; height:50px; background:#e9e1d8; border-radius:8px; display:flex; align-items:center; justify-content:center; overflow:hidden;">
                                ${m.image ? `<img src="${m.image}" style="width:100%; height:100%; object-fit:cover;">` : "🐱"}
                            </div>
                            <div style="flex:1;">
                                <div style="font-weight:600;">${m.name}</div>
                                <div style="font-size:0.7rem; color:#7f5e48;">${m.tags.slice(0, 2).join(", ")}</div>
                            </div>
                        </div>
                    `,
                      )
                      .join("")}
                    ${!eraMemes.length ? '<div style="text-align:center; padding:20px;">Мемов пока нет</div>' : ""}
                </div>
            </div>
        `;
  }).join("");

  if (eraContext) {
    eraContext.innerHTML = `<h3>📖 Контекст эпох</h3><p>${ERAS.map((e) => `<strong>${e.name}:</strong> ${e.context}`).join("<br><br>")}</p>`;
  }

  document.querySelectorAll(".tl-meme-card").forEach((card) => {
    card.addEventListener("click", () => {
      const id = parseInt(card.getAttribute("data-id"));
      if (!isNaN(id)) showMemeDetail(id);
    });
  });
}

function renderTags() {
  const allTags = [...new Set(memesData.flatMap((m) => m.tags))].slice(0, 18);
  const tagCloud = document.getElementById("tagCloud");
  if (tagCloud)
    tagCloud.innerHTML = allTags
      .map((t) => `<span class="tag">#${t}</span>`)
      .join("");

  const rare = memesData
    .filter((m) => !m.similarIds || m.similarIds.length === 0)
    .slice(0, 4);
  const rareList = document.getElementById("rareMemesList");
  if (rareList) {
    rareList.innerHTML = rare.map((m) => renderMemeCard(m, 60)).join("");
    document.querySelectorAll("#rareMemesList .meme-card").forEach((c) => {
      c.addEventListener("click", () => {
        const id = parseInt(c.getAttribute("data-id"));
        if (!isNaN(id)) showMemeDetail(id);
      });
    });
  }
}

